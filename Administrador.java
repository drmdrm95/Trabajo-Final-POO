    private void btnAbrirPreguntasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirPreguntasActionPerformed
        
        if (seleccionado.showDialog(null, "Abrir Preguntas") == JFileChooser.APPROVE_OPTION){ //El "null" posiciona la ventana en la parte del centro, y el titulo a mostrar. Es decir, si seleccionamos un archivo se ejecuta lo siguiente:
        
            preguntas = seleccionado.getSelectedFile(); //Al archivo le damos el valor de seleccionado.
            if (preguntas.canRead()){   //En caso de que ese archivo se puede leer.
            
                if (preguntas.getName().endsWith("txt")){ //Pregunta si es un archivo de texto, osea extenci贸n txt.
                    
                    String contenido = gestionarpreguntas.AbrirPreguntas(preguntas); //Le mandamos el archivo seleccionado para la apertura.
                    txtAreaPreguntas.setText(contenido); //Nos regresa el contenido y se pone dentro del Text Area.
                }else{
                    
                    JOptionPane.showMessageDialog(null, "Por favor seleccione un archivo de texto."); //En caso de que no sea un archivo .txt
                    
                }
            }
            
        }
        
    }//GEN-LAST:event_btnAbrirPreguntasActionPerformed

    //Al presionar el bot贸n "Guardar Preguntas".
    private void btnGuardarPreguntasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPreguntasActionPerformed
        
        if(seleccionado.showDialog(null, "Guardar Preguntas") == JFileChooser.APPROVE_OPTION){
            preguntas = seleccionado.getSelectedFile();
            if(preguntas.getName().endsWith("txt")){ //Si el archivo es .txt se crea como una variable de respuesta a la que se le designa el valor que se recupere de "Guardar Preguntas"
                String contenido = txtAreaPreguntas.getText(); //Se le designa el valor que se recupere del Text Area.
                String respuesta = gestionarpreguntas.GuardarPreguntas(preguntas, contenido); //Le enviamos "contenido"
                if(respuesta!=null){ //Si es diferente de "null"
                    JOptionPane.showMessageDialog(null, respuesta);
                }else{
                    JOptionPane.showMessageDialog(null, "Error al guardar las preguntas."); //En caso de error.
                }
            }else{ //En caso de que no sea el archivo de extenci贸n .txt
                JOptionPane.showMessageDialog(null, "Las preguntas se deben guardar en un formato de texto.");
            }
        }
    }//GEN-LAST:event_btnGuardarPreguntasActionPerformed

