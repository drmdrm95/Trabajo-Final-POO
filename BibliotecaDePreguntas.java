    //El siguiente método para leer la pregunta #1 actua de manera similar para las demás preguntas.
    public void LeerPregunta1(String NombPregunt){
    
        File f;
        FileReader fr;
        BufferedReader br;
                                                /* Se crea un método para cada pregunta el cual al ser invocado leerá el archivo txt correspondiente a esa pregunta,
                                                   los cuales se ejecutan desde el botón "Presentador".*/
        try{
        
            f = new File(NombPregunt);           
            fr = new FileReader(f);
            br = new BufferedReader(fr);
            
            String Linea;                               /*Por medio de la variable Linea se captura todo lo que hay dentro del archivo de texto
                                                          y luego se imprime dicha variable con todo lo que capturó*/
            while((Linea = br.readLine()) != null){     
            
                System.out.println(Linea);
            }
            
        } catch (Exception e){
        
        JOptionPane.showMessageDialog(null, "Ha ocurrido un error."+ e);
        }
           
    }
    
